'''
Crie um programa que peça um número inteiro positivo ao usuário e 
faça uma contagem regressiva a partir desse número até chegar a 0. 
Ao final, imprima "Fogo!".
'''

contagem_regressiva = 3

while contagem_regressiva > 0:
    print(contagem_regressiva)
    contagem_regressiva -= 1

    
print('Fogo!')